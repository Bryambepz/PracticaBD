    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        // TODO add your handling code here:
//        int id = Integer.parseInt(txt1.getText());
        String cedula = txt2.getText();
        String nombre = txt3.getText();
        String direccion = txt4.getText();
        String telefono = txt5.getText();
        int diaI = Integer.parseInt(txt6_1.getText());
        int mesI = Integer.parseInt(txt6_2.getText());
        int anioI = Integer.parseInt(txt6_3.getText());
        Date fechaI = new Date(anioI-1900, mesI, diaI);
        int diaF = Integer.parseInt(txt7_1.getText());
        int mesF = Integer.parseInt(txt7_2.getText());
        int anioF = Integer.parseInt(txt7_3.getText());
        Date fechaF = new Date(anioF-1900, mesF, diaF);
        String estado = txt8.getText();
        
        if (cedula.isBlank() || nombre.isBlank() || direccion.isBlank() || telefono.isBlank() || estado.isBlank()) {
            JOptionPane.showMessageDialog(this, "Ingrese todos los campos");
        }else{
            var cli = new Cliente(ctrlCliente.id(), cedula, nombre, direccion, telefono, fechaI, fechaF, estado);
            ctrlCliente.create(cli);
            ctrlCliente.read();
            System.out.println(ctrlCliente.findAll().size());
            JOptionPane.showMessageDialog(this, "Creado correctamente");
            txt1.setText(String.valueOf(ctrlCliente.id()));
            limpiar();
        }
    }//GEN-LAST:event_btnCrearActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
//        txt1.setEditable(true);
        String cedula = txt2.getText();
        String nombre = txt3.getText();
        String direccion = txt4.getText();
        String telefono = txt5.getText();
        int diaI = Integer.parseInt(txt6_1.getText());
        int mesI = Integer.parseInt(txt6_2.getText());
        int anioI = Integer.parseInt(txt6_3.getText());
        Date fechaI = new Date(anioI+1900, mesI, diaI);
        int diaF = Integer.parseInt(txt7_1.getText());
        int mesF = Integer.parseInt(txt7_2.getText());
        int anioF = Integer.parseInt(txt7_3.getText());
        Date fechaF = new Date(anioF+1900, mesF, diaF);
        String estado = txt8.getText();
        
        if (cedula.isBlank() || nombre.isBlank() || direccion.isBlank() || telefono.isBlank() || estado.isBlank()) {
            JOptionPane.showMessageDialog(this, "Ingrese todos los campos");
        }else{
            var cli = new Cliente();
            cli.setId(Integer.parseInt(txt1.getText()));
            cli.setCedula(cedula);
            cli.setNombre(nombre);
            cli.setDireccion(direccion);
            cli.setTelefono(telefono);
            cli.setFecha_Inicio(fechaI);
            cli.setFecha_Fin(fechaF);
            cli.setEstado_Tramite(estado);
            ctrlCliente.update(cli);
            ctrlCliente.read();
            ctrlCliente.imprimir();
            System.out.println(ctrlCliente.findAll().size());
            JOptionPane.showMessageDialog(this, "Actualizado correctamente");
            txt1.setText(String.valueOf(ctrlCliente.id()));
            limpiar();
            txt1.setEditable(false);
            btnUpdate.setEnabled(false);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed


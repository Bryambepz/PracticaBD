    public void llenartabla(List<Cliente> listado){
        modelo = (DefaultTableModel) tblInfo.getModel();
        modelo.setRowCount(0);
        for (int i = 0; i < listado.size(); i++) {
            var datosCL = listado.get(i);
            Object[] cli= {datosCL.getId(), datosCL.getCedula(), datosCL.getNombre(), datosCL.getDireccion(), datosCL.getTelefono(), datosCL.getFecha_Inicio().toString(), datosCL.getFecha_Fin().toString(), datosCL.getEstado_Tramite()};
            modelo.addRow(cli);
        }
        tblInfo.setModel(modelo);
    }
